// 카테고리별 상품 필터링 함수
export const getProductsByCategory = (category = "전체") => {
  if (category === "전체") return dummyProducts;
  return dummyProducts.filter((product) => product.category === category);
};

// 페이지네이션을 위한 상품 가져오기 함수
export const getProductsWithPagination = (
  category = "전체",
  page = 1,
  limit = 12
) => {
  const products = getProductsByCategory(category);
  const startIndex = (page - 1) * limit;
  const endIndex = startIndex + limit;

  return {
    products: products.slice(startIndex, endIndex),
    hasMore: endIndex < products.length,
    total: products.length,
  };
};
